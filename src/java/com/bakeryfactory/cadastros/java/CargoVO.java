package com.bakeryfactory.cadastros.java;

import java.io.Serializable;
import java.math.BigDecimal;
import java.util.List;
import javax.persistence.Basic;
import javax.persistence.CascadeType;
import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.JoinColumn;
import javax.persistence.ManyToOne;
import javax.persistence.NamedQueries;
import javax.persistence.NamedQuery;
import javax.persistence.OneToMany;
import javax.persistence.Table;
import org.hibernate.annotations.LazyCollection;
import org.hibernate.annotations.LazyCollectionOption;
import org.openswing.swing.message.receive.java.ValueObjectImpl;

/**
 *
 * @author Claudinei Aparecido Perboni • contact: cperbony@gmail.com
 */
@Entity
@Table(name = "CARGO")
public class CargoVO extends ValueObjectImpl implements Serializable {

    private static final long serialVersionUID = 1L;
    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    @Basic(optional = false)
    @Column(name = "ID")
    private Integer id;
    @Column(name = "NOME")
    private String nome;
    @Column(name = "DESCRICAO")
    private String descricao;
    // @Max(value=?)  @Min(value=?)//if you know range of your decimal fields consider using these annotations to enforce field validation
    @Column(name = "SALARIO")
    private BigDecimal salario;
    @Column(name = "CBO_1994")
    private String cbo1994;
    @Column(name = "CBO_2002")
    private String cbo2002;
    
    @OneToMany(cascade = CascadeType.ALL, orphanRemoval = true, mappedBy = "cargo")
    @LazyCollection(LazyCollectionOption.FALSE)
    private List<ColaboradorVO> listaColaborador;
    
    @JoinColumn(name = "ID_EMPRESA", referencedColumnName = "ID")
    @ManyToOne(optional = false)
    private EmpresaVO empresa;

    public CargoVO() {
    }

    public CargoVO(Integer id) {
        this.id = id;
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getDescricao() {
        return descricao;
    }

    public void setDescricao(String descricao) {
        this.descricao = descricao;
    }

    public BigDecimal getSalario() {
        return salario;
    }

    public void setSalario(BigDecimal salario) {
        this.salario = salario;
    }

    public List<ColaboradorVO> getListaColaborador() {
        return listaColaborador;
    }

    public void setListaColaborador(List<ColaboradorVO> listaColaborador) {
        this.listaColaborador = listaColaborador;
    }

    public EmpresaVO getEmpresa() {
        return empresa;
    }

    public void setEmpresa(EmpresaVO empresa) {
        this.empresa = empresa;
    }

    public String getCbo1994() {
        return cbo1994;
    }

    public void setCbo1994(String cbo1994) {
        this.cbo1994 = cbo1994;
    }

    public String getCbo2002() {
        return cbo2002;
    }

    public void setCbo2002(String cbo2002) {
        this.cbo2002 = cbo2002;
    }
    
    
    

    @Override
    public String toString() {
        return "com.bakeryfactory.cadastros.java.CargoVO[ id=" + id + " ]";
    }

}
